package com.topshop.dto;

public class Member {
	private String mId = null;
	private String mPw = null;
	private String mName = null;
	private int mLevel = 0;
	private String mAddr = null;
	private int mAge = 0;
	private String mPhone = null;
	private String mEmail = null;
	
	public String getmId() {
		return mId;
	}
	public Member setmId(String mId) {
		this.mId = mId;
		return this;
	}
	public String getmPw() {
		return mPw;
	}
	public Member setmPw(String mPw) {
		this.mPw = mPw;
		return this;
	}
	public String getmName() {
		return mName;
	}
	public Member setmName(String mName) {
		this.mName = mName;
		return this;
	}
	public int getmLevel() {
		return mLevel;
	}
	public Member setmLevel(int mLevel) {
		this.mLevel = mLevel;
		return this;
	}
	public String getmAddr() {
		return mAddr;
	}
	public Member setmAddr(String mAddr) {
		this.mAddr = mAddr;
		return this;
	}
	public int getmAge() {
		return mAge;
	}
	public Member setmAge(int mAge) {
		this.mAge = mAge;
		return this;
	}
	public String getmPhone() {
		return mPhone;
	}
	public Member setmPhone(String mPhone) {
		this.mPhone = mPhone;
		return this;
	}
	public String getmEmail() {
		return mEmail;
	}
	public Member setmEmail(String mEmail) {
		this.mEmail = mEmail;
		return this;
	}
	
	
}
